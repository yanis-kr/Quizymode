# (question, correct, (w1,w2,w3), explanation, kw1, kw2, source)
T = tuple[str, str, tuple[str, str, str], str, str, str, str]


def pack(l1: str, l2: str, items: list[T]) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    for q, ca, w, ex, k1, k2, src in items:
        rows.append(
            {
                "category": "business",
                "navigationKeyword1": l1,
                "navigationKeyword2": l2,
                "question": q,
                "correctAnswer": ca,
                "incorrectAnswers": list(w),
                "explanation": ex,
                "keywords": [k1, k2],
                "source": src,
                "itemId": str(uuid.uuid4()),
            }
        )
    return rows


def emit(l1: str, l2: str, items: list[T]) -> bool:
    OUT.mkdir(parents=True, exist_ok=True)
    path = OUT / f"business.{l1}.{l2}.json"
    if path.exists():
        return False
    path.write_text(json.dumps(pack(l1, l2, items), ensure_ascii=True, indent=2) + "\n", encoding="utf-8")
    return True


